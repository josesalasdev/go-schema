// extractIntValue extracts an int64 value from different numeric types
func extractIntValue(value interface{}) (int64, bool) {
	switch v := value.(type) {
	case int:
		return int64(v), true
	case int8:
		return int64(v), true
	case int16:
		return int64(v), true
	case int32:
		return int64(v), true
	case int64:
		return v, true
	case uint:
		return int64(v), true
	case uint8:
		return int64(v), true
	case uint16:
		return int64(v), true
	case uint32:
		return int64(v), true
	case uint64:
		return int64(v), true
	case float32:
		if float32(int64(v)) == v {
			return int64(v), true
		}
	case float64:
		if float64(int64(v)) == v {
			return int64(v), true
		}
	}
	return 0, false
}

